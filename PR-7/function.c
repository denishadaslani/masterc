int Addtion(int first, int second)
{
    return first + second;
}

int Subtraction(int first, int second)
{
    return first - second;
}

int Multiplication(int first, int second)
{
    return first * second;
}
int Division(int first, int second)
{
    return first / second;
}
int Module(int first, int second)
{
    return first % second;
}